/* 

'int' digunakan untuk bilangan bulat

'double' digunakan untuk bilangan desimal; penggunaan
koma diganti dengan tanda titik sesuai dengan
standar internasional

'num' bisa diubah-ubah antara int atau double

*/

void main() {
  int number1 = 10;
  double number2 = 20.5;
  num number3 = 30;

  print(number1);
  print(number2);

  number3 = 10.544;
  print(number3);
}
