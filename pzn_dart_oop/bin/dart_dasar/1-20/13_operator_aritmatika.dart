void main() {
  /* 
  
  penggunaan aritmatika sama dengan operator
  matematika pada umumnya
  
  */

  var first = 10;
  var second = 4;

  print(first + second);
  print(first / second);
  print(first - second);
  print(first * second);
  print(first ~/ second);
  print(first % second);
}
